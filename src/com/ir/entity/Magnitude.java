package com.ir.entity;

public class Magnitude {
	long tf;
	long df;
	double idf;
	public long getTf() {
		return tf;
	}
	public void setTf(long tf) {
		this.tf = tf;
	}
	public long getDf() {
		return df;
	}
	public void setDf(long df) {
		this.df = df;
	}
	public double getIdf() {
		return idf;
	}
	public void setIdf(double idf) {
		this.idf = idf;
	}
}
